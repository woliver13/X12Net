using Microsoft.CodeAnalysis;

namespace X12Net.SourceGenerator;

/// <summary>
/// Roslyn incremental source generator that emits strongly-typed X12 transaction-set
/// classes and their key segment wrappers for all built-in schemas.
/// </summary>
[Generator]
public sealed class X12TransactionGenerator : IIncrementalGenerator
{
    // ── Descriptor tables ─────────────────────────────────────────────────

    // Each entry: (segmentId, className, prop1Name, prop1DocElement, prop2Name, prop2DocElement, prop3Name, prop3DocElement)
    // Props map to element index 1, 2, 4 respectively (0 means "not exposed").
    private static readonly SegmentDesc[] Segments =
    {
        new("AK1", "AK1Segment",
            "FunctionalIdentifierCode", 1, "Functional Identifier Code",
            "GroupControlNumber",        2, "Group Control Number",
            null, 0, null),

        new("CLP", "CLPSegment",
            "PatientControlNumber", 1, "Patient Control Number",
            "ClaimStatusCode",      2, "Claim Status Code",
            null, 0, null),

        new("CLM", "ClmSegment",
            "PatientControlNumber",     1, "Patient Control Number (CLM01)",
            "TotalClaimChargeAmount",   2, "Total Claim Charge Amount (CLM02)",
            null, 0, null),

        new("EQ", "EQSegment",
            "ServiceTypeCode", 1, "Service Type Code (EQ01)",
            null, 0, null,
            null, 0, null),

        new("EB", "EBSegment",
            "EligibilityOrBenefitInformation", 1, "Eligibility or Benefit Information (EB01)",
            null, 0, null,
            null, 0, null),

        new("INS", "INSSegment",
            "MemberIndicator",             1, "Member Indicator (INS01)",
            "IndividualRelationshipCode",  2, "Individual Relationship Code (INS02)",
            null, 0, null),

        new("STC", "STCSegment",
            "HealthCareClaimStatus", 1, "Health Care Claim Status composite (STC01)",
